import zipfile
import os
import pandas as pd
from google.colab import files

# ============================================================================
# STEP 1: UPLOAD AND EXTRACT ZIP FILE
# ============================================================================
# Prompts user to upload a zip file containing Excel spreadsheets
print("Please upload your zip file containing Excel files:")
uploaded = files.upload()

# Get the filename of the uploaded zip
zip_filename = list(uploaded.keys())[0]
print(f"Uploaded file: {zip_filename}")

# Extract all contents to /content/dataset directory
with zipfile.ZipFile(zip_filename, 'r') as zip_ref:
    zip_ref.extractall('/content/dataset')
print("Zip file extracted successfully to '/content/dataset'")

# ============================================================================
# STEP 2: READ AND CONSOLIDATE ALL EXCEL FILES
# ============================================================================
def read_excel_files(directory):
    """
    Recursively traverses directory, reads all Excel files and their sheets,
    adds source metadata, and consolidates into one DataFrame.
    
    Args:
        directory (str): Root directory path to search for Excel files
    
    Returns:
        pd.DataFrame: Consolidated dataframe with all data
    """
    all_dataframes = []  # List to store individual dataframes
    
    # Recursively walk through all directories and subdirectories
    for root, dirs, files in os.walk(directory):
        for file in files:
            # Check if file is Excel (.xlsx or .xls)
            if file.endswith('.xlsx') or file.endswith('.xls'):
                file_path = os.path.join(root, file)
                print(f"Reading file: {file_path}")
                
                # Load the Excel file
                xls = pd.ExcelFile(file_path)
                
                # Iterate through each sheet in the Excel file
                for sheet_name in xls.sheet_names:
                    print(f"  Reading sheet: {sheet_name}")
                    
                    # Read the sheet into a DataFrame
                    df = pd.read_excel(xls, sheet_name=sheet_name)
                    
                    # Add metadata columns for traceability
                    df['Source Filename'] = file      # Which file this row came from
                    df['Source Sheet Name'] = sheet_name  # Which sheet this row came from
                    
                    # Append to list
                    all_dataframes.append(df)
    
    # Concatenate all dataframes into one (ignore index, create new sequential index)
    consolidated_df = pd.concat(all_dataframes, ignore_index=True)
    return consolidated_df

# Execute the consolidation
print("\nStarting consolidation process...")
consolidated_data = read_excel_files('/content/dataset')

# Display first few rows to verify
print("\nConsolidated Data Preview (first 5 rows):")
